package com.examples.mycalculator;

import android.content.Context;
import android.widget.Toast;

public class Calculate {

    public static int Add(Context context,int first,int secound)
    {
        return first+secound;
    }

    public static int Subtract(Context context,int first,int secound)
    {
        return first-secound;
    }

    public static int Multiply(Context context,int first,int secound)
    {
        return first*secound;
    }

}
